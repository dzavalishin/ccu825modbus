package ru.dz.mercury;

public class Mercury230ProtocolException extends Exception {

	public Mercury230ProtocolException() {
	}

	public Mercury230ProtocolException(String message) {
		super(message);
	}

	public Mercury230ProtocolException(Throwable cause) {
		super(cause);
	}

	public Mercury230ProtocolException(String message, Throwable cause) {
		super(message, cause);
	}

	public Mercury230ProtocolException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
