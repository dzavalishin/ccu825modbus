	@Override
	public byte[] rwMultiple(int nRead, byte[] writeData) {
		
		byte[] answer = makeAnswer(writeData);
		
		byte[] ret = new byte[nRead*2];
		
		
		int len = Math.min(answer.length, ret.length );
		System.arraycopy(answer, 0, ret, 0, len);
		
		return ret;
	}

	private byte[] makeAnswer(byte[] writeData) {
		
		//CCU825Test.dumpBytes("pkt to send", writeData);
		
		if(writeData.length == 8)
		{
			// Got empty packet, this means sync
			return synAckPacket; 
		}

		if( (writeData.length == 10) && (writeData[8] == 0x01) && (writeData[8] == 0x01) )
		{
			// Dev info req
			return deviceInfoPacket; 
		}
			
		
		if( (writeData.length == 12) && (writeData[8] == 0x2A) && (writeData[9] == -80 /*0xB0*/) && (writeData[10] == -119/*0x89*/) )
		{
			// rc
			return rcPacket; 
		}
		
		return new byte[0];
	}


